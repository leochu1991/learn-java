package behavioral_patterns.memento_pattern;

/**
 * 发起人类，创建状态并保存
 */
public class Originator {
    private String state;

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }

    public Memento saveStateToMemento() {
        return new Memento(state);
    }
}
